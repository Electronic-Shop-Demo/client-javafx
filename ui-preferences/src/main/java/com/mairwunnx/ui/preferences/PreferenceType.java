package com.mairwunnx.ui.preferences;

/**
 * Preference type of properties will be loaded.
 *
 * @since 1.0.0.
 */
public enum PreferenceType {
    /**
     * XML Properties type.
     *
     * @since 1.0.0.
     */
    XML,
    /**
     * Properties properties type. (Plain java properties).
     *
     * @since 1.0.0.
     */
    PROPERTIES
}
